package mypackage1;

public class Day2_ComparsionOperators {
	
	public static void main(String[] args) {
		
		int a=10;
		int b=20;
		
//the return type of these operators will be boolean[true, false]
		//is a greater than b         >
		//is a less than b            <
		//is a equal to b             ==
		//is a not equal to b         !=
		//is a less or equal to b     <= 
		//is a greater or equal to b  >=
		
		System.out.println(a>b);     //false
		System.out.println(a<b);     //true
		System.out.println(a<=b);    //true
		System.out.println(a>=b);    //false
		System.out.println(a==b);    //false
		System.out.println(a!=b);    //true
	}

}
