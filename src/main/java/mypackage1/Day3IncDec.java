package mypackage1;

public class Day3IncDec {

	public static void main(String[] args) {
   
		//inc  -> ++
		//dec. -> --
		
		//increment , add 1 to the current number
		//decrement , sub 1 to the current number
		
		
		int a=10;
		a++;   //add 1 to a ; 
		System.out.println(a);
		
		int b=20;
		b--;      //dec. 1 to b , 19
		System.out.println(b);
	}

}
